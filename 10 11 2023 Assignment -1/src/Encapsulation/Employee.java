package Encapsulation;

public class Employee {
	
	private int eid;
	private String ename;
	private double esal;
	private Eproject[] ep;
	
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public double getEsal() {
		return esal;
	}
	public void setEsal(double esal) {
		this.esal = esal;
	}
	public Eproject[] getEp() {
		return ep;
	}
	public void setEp(Eproject[] ep) {
		this.ep = ep;
	}
	 
	
	

}
