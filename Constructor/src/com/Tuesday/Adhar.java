package com.Tuesday;

public class Adhar {
	 
	
	public Adhar() {
		System.out.println("Default Constructor");
	}
	public Adhar(int aid, String aloc, long ano) {
	    System.out.println(" 3 Argumented Constructor");
		this.aid = aid;
		this.aloc = aloc;
		this.ano = ano;
	}
	
	private int aid;
	private String aloc;
	private long ano;
	
	
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public String getAloc() {
		return aloc;
	}
	public void setAloc(String aloc) {
		this.aloc = aloc;
	}
	public long getAno() {
		return ano;
	}
	public void setAno(long ano) {
		this.ano = ano;
	}
	 

}
