package com.relationsAADHARandMobile;

public class Panper {
	
	private int pid;
	private String pname;
	private int pad;
	private Pancard pan;
	private Car[] ca;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPad() {
		return pad;
	}
	public void setPad(int pad) {
		this.pad = pad;
	}
	public Pancard getPan() {
		return pan;
	}
	public void setPan(Pancard pan) {
		this.pan = pan;
	}
	public Car[] getCa() {
		return ca;
	}
	public void setCa(Car[] ca) {
		this.ca = ca;
	}

}
