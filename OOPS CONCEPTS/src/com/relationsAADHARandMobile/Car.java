package com.relationsAADHARandMobile;

public class Car {
	
	private int cid;
	private String cb;
	private String cn;
	private double cra;
	
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCb() {
		return cb;
	}
	public void setCb(String cb) {
		this.cb = cb;
	}
	public String getCn() {
		return cn;
	}
	public void setCn(String cn) {
		this.cn = cn;
	}
	public double getCra() {
		return cra;
	}
	public void setCra(double cra) {
		this.cra = cra;
	}
	 

}
