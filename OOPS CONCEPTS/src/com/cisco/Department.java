package com.cisco;

public class Department {
	
	private int dno;
	private String dname;
	private int dran;
	public int getDno() {
		return dno;
	}
	public void setDno(int dno) {
		this.dno = dno;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public int getDran() {
		return dran;
	}
	public void setDran(int dran) {
		this.dran = dran;
	}

}
