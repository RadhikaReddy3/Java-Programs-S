package com.vyaire;

public class Course {
	private int cid;
	private String cname;
	private String cdur;
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCdur() {
		return cdur;
	}
	public void setCdur(String cdur) {
		this.cdur = cdur;
	}
	 

}
